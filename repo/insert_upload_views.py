            "                            self.active_view = ActiveView::UploadTopicSelect;\n",
            "                        }\n",
            "                        crossterm::event::KeyCode::Enter => {\n",
            "                            if !self.upload_topic_name_input.is_empty() {\n",
            "                                let tg = std::sync::Arc::clone(telegram);\n",
            "                                let tx_clone = tx.clone();\n",
            "                                let group_id = self.upload_dest_group_id.unwrap();\n",
            "                                let title = self.upload_topic_name_input.clone();\n",
            "                                tokio::spawn(async move {\n",
            "                                    match tg.create_topic(group_id, &title).await {\n",
            "                                        Ok(id) => {\n",
            "                                            let _ = tx_clone.send(AppEvent::TopicCreated(id, title)).await;\n",
            "                                        }\n",
            "                                        Err(e) => {\n",
            "                                            // For now just error as topicsloaded\n",
            "                                            let _ = tx_clone.send(AppEvent::TopicsLoaded(Err(e.to_string()))).await;\n",
