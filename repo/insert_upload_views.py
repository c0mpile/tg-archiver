            "                                    self.active_view = ActiveView::UploadTopicNameEntry;\n",
            "                                } else if let Some((id, title)) = self.available_topics.get(i - 1) {\n",
            "                                    self.upload_dest_topic_id = Some(*id);\n",
            "                                    self.upload_dest_topic_title = Some(title.clone());\n",
