            "                                let tx_clone = tx.clone();\n",
            "                                tokio::spawn(async move {\n",
            "                                    let res = tg.get_joined_groups().await.map_err(|e| e.to_string());\n",
            "                                    let _ = tx_clone.send(AppEvent::GroupsLoaded(res)).await;\n",
            "                                });\n",
            "                            }\n",
            "                        }\n",
            "                        crossterm::event::KeyCode::Esc => {\n",
            "                            self.active_view = ActiveView::UploadModeSelect;\n",
            "                        }\n",
            "                        _ => {}\n",
            "                    },\n",
            "                    ActiveView::UploadGroupSelect => match key.code {\n",
            "                        crossterm::event::KeyCode::Down | crossterm::event::KeyCode::Char('j') => {\n",
            "                            if !self.available_groups.is_empty() {\n",
            "                                let i = match self.group_list_state.selected() {\n",
            "                                    Some(i) => if i >= self.available_groups.len() - 1 { i } else { i + 1 },\n",
            "                                    None => 0,\n",
            "                                };\n",
            "                                self.group_list_state.select(Some(i));\n",
            "                            }\n",
            "                        }\n",
            "                        crossterm::event::KeyCode::Up | crossterm::event::KeyCode::Char('k') => {\n",
            "                            if !self.available_groups.is_empty() {\n",
            "                                let i = match self.group_list_state.selected() {\n",
            "                                    Some(i) => if i == 0 { 0 } else { i - 1 },\n",
            "                                    None => 0,\n",
            "                                };\n",
            "                                self.group_list_state.select(Some(i));\n",
            "                            }\n",
            "                        }\n",
            "                        crossterm::event::KeyCode::Esc => {\n",
            "                            self.active_view = ActiveView::UploadFileSelect;\n",
