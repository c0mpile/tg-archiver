            "                                });\n",
            "                            }\n",
            "                        }\n",
            "                        _ => {}\n",
            "                    },\n",
            "                    ActiveView::UploadTopicSelect => match key.code {\n",
            "                        crossterm::event::KeyCode::Down | crossterm::event::KeyCode::Char('j') => {\n",
