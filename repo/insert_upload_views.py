            "                        crossterm::event::KeyCode::Char('s') | crossterm::event::KeyCode::Char('S') => {\n",
            "                            let _ = tx.try_send(AppEvent::UploadModeSelected(UploadMode::Select));\n",
            "                        }\n",
            "                        crossterm::event::KeyCode::Char('y') | crossterm::event::KeyCode::Char('Y') => {\n",
            "                            let _ = tx.try_send(AppEvent::UploadModeSelected(UploadMode::Sync));\n",
            "                        }\n",
            "                        crossterm::event::KeyCode::Esc => {\n",
            "                            self.active_view = ActiveView::Home;\n",
            "                        }\n",
            "                        _ => {}\n",
            "                    },\n",
            "                    ActiveView::UploadSyncResume => match key.code {\n",
            "                        crossterm::event::KeyCode::Char('y') | crossterm::event::KeyCode::Char('Y') | crossterm::event::KeyCode::Enter => {\n",
            "                            self.upload_dest_group_id = Some(self.upload_sync_state.as_ref().unwrap().dest_group_id);\n",
