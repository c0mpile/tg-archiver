            "                            // Filter out already uploaded files from selection\n",
            "                            if let Some(state) = &self.upload_sync_state {\n",
            "                                for (i, entry) in self.upload_entries.iter().enumerate() {\n",
            "                                    if let UploadEntry::File { name, size_bytes, .. } = entry {\n",
            "                                        if !state.uploaded_files.iter().any(|f| f.filename == *name && f.size_bytes >= *size_bytes) {\n",
            "                                            self.upload_selected[i] = true;\n",
            "                                        }\n",
            "                                    }\n",
            "                                }\n",
            "                            }\n",
            "                            self.active_view = ActiveView::UploadFileSelect;\n",
            "                        }\n",
            "                        crossterm::event::KeyCode::Char('n') | crossterm::event::KeyCode::Char('N') => {\n",
            "                            self.upload_sync_state = None;\n",
            "                            self.active_view = ActiveView::UploadFileSelect;\n",
            "                        }\n",
            "                        crossterm::event::KeyCode::Esc => {\n",
            "                            self.active_view = ActiveView::Home;\n",
            "                        }\n",
            "                        _ => {}\n",
            "                    },\n",
            "                    ActiveView::UploadFileSelect => match key.code {\n",
            "                        crossterm::event::KeyCode::Down | crossterm::event::KeyCode::Char('j') => {\n",
            "                            if !self.upload_entries.is_empty() {\n",
            "                                let i = match self.upload_list_state.selected() {\n",
            "                                    Some(i) => if i >= self.upload_entries.len() - 1 { i } else { i + 1 },\n",
            "                                    None => 0,\n",
            "                                };\n",
            "                                self.upload_list_state.select(Some(i));\n",
            "                            }\n",
            "                        }\n",
            "                        crossterm::event::KeyCode::Up | crossterm::event::KeyCode::Char('k') => {\n",
            "                            if !self.upload_entries.is_empty() {\n",
            "                                let i = match self.upload_list_state.selected() {\n",
            "                                    Some(i) => if i == 0 { 0 } else { i - 1 },\n",
            "                                    None => 0,\n",
            "                                };\n",
            "                                self.upload_list_state.select(Some(i));\n",
            "                            }\n",
            "                        }\n",
            "                        crossterm::event::KeyCode::Char(' ') => {\n",
            "                            if let Some(i) = self.upload_list_state.selected() {\n",
            "                                let _ = tx.try_send(AppEvent::UploadFileToggled(i));\n",
            "                            }\n",
            "                        }\n",
            "                        crossterm::event::KeyCode::Char('a') => {\n",
            "                            let _ = tx.try_send(AppEvent::UploadSelectAll);\n",
            "                        }\n",
            "                        crossterm::event::KeyCode::Char('t') => {\n",
            "                            let _ = tx.try_send(AppEvent::UploadSortToggle);\n",
            "                        }\n",
            "                        crossterm::event::KeyCode::Enter => {\n",
            "                            if self.upload_sync_state.is_some() {\n",
            "                                let _ = tx.try_send(AppEvent::StartUploadRun);\n",
            "                            } else {\n",
            "                                self.active_view = ActiveView::UploadGroupSelect;\n",
