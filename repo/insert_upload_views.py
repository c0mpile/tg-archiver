            "                                };\n",
            "                                self.topic_list_state.select(Some(i));\n",
            "                            }\n",
            "                        }\n",
            "                        crossterm::event::KeyCode::Up | crossterm::event::KeyCode::Char('k') => {\n",
            "                            if !self.available_topics.is_empty() {\n",
            "                                let i = match self.topic_list_state.selected() {\n",
            "                                    Some(i) => if i == 0 { 0 } else { i - 1 },\n",
            "                                    None => 0,\n",
            "                                };\n",
            "                                self.topic_list_state.select(Some(i));\n",
            "                            }\n",
            "                        }\n",
            "                        crossterm::event::KeyCode::Esc => {\n",
            "                            self.active_view = ActiveView::UploadGroupSelect;\n",
